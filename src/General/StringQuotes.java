package General;

public class StringQuotes {

	public static void main(String[] args) {
		//  "Hello"
		// \Hello\
		//  i love "java" programming and "movies"


		System.out.println("\"Hello\"");
		
		System.out.println("\\Hello\\");
			
		System.out.println("i love \"java\" programming and \"movies\"");
	}

}
